    public static void showMedinJcombo(JComboBox combo ) throws SQLException{
        ResultSet rs= getMedList();
        
        combo.removeAllItems();
        while(rs.next())  {
            combo.addItem(rs.getString("nomMedicament")+" _"+rs.getString("idMedicament"));
        }
        
        
    }
    public ResultSet getMed() throws SQLException{
        String sql = "SELECT Medicament.idmedicament,nomMedicament,typeMed,qte, prixAchat, prixvente, dateE from Medicament "
                + "inner join gestMedicament on Medicament.idMedicament = gestMedicament.idMedicament ";
        Statement stmt =Main.getConnection(Main.jdbc).createStatement();
        return stmt.executeQuery(sql);
    }
    public void showMedinTable() throws SQLException{
        ResultSet rs=getMed();
        DefaultTableModel model=(DefaultTableModel) jTable_med.getModel();
        model.setRowCount(0);
        
             String id;        
             String nom;       
             String type;        
             String qte;      
             String pachat;        
             String pvente;        
             String dateE;
           
        
        while(rs.next()){
            id = rs.getString("idmedicament");
            medicament[0].add(id);
            nom = rs.getString("nomMedicament");
            medicament[1].add(nom);
            type = rs.getString("typeMed");
            qte = rs.getString("qte");
            pachat = rs.getString("prixAchat"); 
            medicament[2].add(pachat);
            pvente = rs.getString("prixvente");
            dateE = rs.getString("dateE");
            if(Integer.parseInt(qte)>0){
            model.insertRow(model.getRowCount(),new Object []{
                id,nom,type,qte,pachat,pvente,dateE
                
            }
            );
            }            
        }
        
        
    }
    
