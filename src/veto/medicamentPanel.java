    private void jPanel_ajouterStockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_ajouterStockMouseClicked
        // TODO add your handling code here:
        
            SimpleDateFormat dtformat= new SimpleDateFormat("dd-MMMM-yyyy");
            String dateE="";
            String dateF="";
            String prixAchat="";
            String prixVente="";
            String qte=jSpinner_qte.getValue().toString();
            
            if(jDate_E.getDate()!=null)
                dateE= dtformat.format(jDate_E.getDate());
            if(jDate_F.getDate()!=null)
                dateF= dtformat.format(jDate_F.getDate());
            String today=dtformat.format(new Date());
            prixAchat=jText_achat.getText();
            prixVente=jText_vente.getText();
            
            
            if(selectedMed>0 && dateF!="" && dateE!="" && prixVente!="" && Integer.parseInt(qte)>0){
                jLabel_notes.setText("");
                String sql="insert into gestMedicament(idMedicament,qte,prixAchat,prixVente,dateAchat,dateE,dateF) values("
                        + selectedMed+","
                        + "'"+qte+"',"
                        + "'"+prixAchat+"',"
                                + "'"+prixVente+"',"
                                + "'"+today+"',"
                                + "'"+dateE+"',"
                                + "'"+dateF+"')";
                Statement stmt;
                try {
                    stmt = Main.getConnection(Main.jdbc).createStatement();
                     stmt.execute(sql);
                     showMedinTable();
                } catch (SQLException ex) {
                    Logger.getLogger(medicamentPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
               
                
                
                
            }
            else {
                jLabel_notes.setText("veuillez remplir tout les champs");
            }
    }//GEN-LAST:event_jPanel_ajouterStockMouseClicked

