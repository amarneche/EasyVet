    public void showConsultationJtable(JTable table) throws SQLException{
        DefaultTableModel model=(DefaultTableModel) table.getModel();
        ResultSet rs=getConsultation();
        model.setRowCount(0);
        while(rs.next()){
            String id=rs.getString("idConsultation");
            String idAnimal=rs.getString("idAnimal");
            String type=rs.getString("type");
            String prix=rs.getString("frais");
            String date=rs.getString("date");
            String heure=rs.getString("heure");
            model.insertRow(model.getRowCount(), new Object[]{
                id,idAnimal,type,prix,date,heure
            });
            
        }
        
    }
